namespace CloudCalc
{
    public static class CalcCore
    {
        public static double Add(double x, double y)
        {
            return x + y;
        }

        public static double Sub(double x, double y)
        {
            return x - y;
        }

        public static double Mult(double x, double y)
        {
            return x*y;
        }

        public static double Div(double x, double y)
        {
            return x/y;
        }
    }
}